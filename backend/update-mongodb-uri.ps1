# MongoDB Connection String Updater
# Usage: Update MONGODB_URI in .env file
# 
# To update manually, edit backend/.env and change:
# MONGODB_URI=your-connection-string-here
#
# Example for MongoDB Atlas:
# MONGODB_URI=mongodb+srv://username:password@cluster0.xxxxx.mongodb.net/vaalhr?retryWrites=true&w=majority
#
# Example for local MongoDB:
# MONGODB_URI=mongodb://localhost:27017/vaalhr
